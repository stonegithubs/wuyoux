     <h1>总则</h1>
        <b></b>
   </div>
<h1>
第一条
</h1>
<p> 本保险合同由保险条款、投保单、保险单、保险凭证以及批单等组成。凡涉及本保险合同的约定， 均应采用书面形式。</p>
<h1>
第二条
</h1>
<p>凡符合保险人承保条件的的自然人均可成为本保险合同的被保险人。</p>
<h1>
第三条
</h1>
<p>本保险合同的投保人应为具有完全民事行为能力的被保险人本人、对被保险人有保险利益的其他人。</p>
<h1>
第四条
</h1>
<p>本保险合同的受益人包括：</p>
<p>（一）身故保险金受益人</p>
<p>订立本保险合同时，被保险人或投保人可指定一人或数人为身故保险金受益人。身故保险金受益人为数人时， 应确定其受益顺序和受益份额；未确定受益份额的，各身故保险金受益人按照相等份额享有受益权。投保人指定受益人时须经被保险人同意。
    被保险人死亡后，有下列情形之一的，保险金作为被保险人的遗产，由保险人依照《中华人民共和国继承法》的规定履行给付保险金的义务：</p>
<p>1.没有指定受益人，或者受益人指定不明无法确定的；</p>
<p>2.受益人先于被保险人死亡，没有其他受益人的；</p>
<p>3.受益人依法丧失受益权或者放弃受益权，没有其他受益人的。</p>
<p>受益人与被保险人在同一事件中死亡，且不能确定死亡先后顺序的，推定受益人死亡在先。
    被保险人或投保人可以变更身故保险金受益人，但需书面通知保险人，由保险人在本保险合同上批注。对因身故保险金受益人变更发生的法律纠纷，保险人不承担任何责任。
    投保人指定或变更身故保险金受益人的，应经被保险人书面同意。被保险人为无民事行为能力人或限制民事行为能力人的，应由其监护人指定或变更身故保险金受益人。</p>
<p>（二）伤残保险金、医疗保险金受益人</p>
<p>除另有约定外，本保险合同的伤残保险金、医疗保险金的受益人为被保险人本人。</p>

   <div class="line">
